$("#aboutbuttonmore").click(function (e) {
    $('#aboutmin').hide();
    $('#mission').hide();
    $('#vision').hide();
    $('#aboutmax').show();
    $('#aboutbuttonmore').hide();
    $('#aboutbuttonminimize').show();
    anime({
        targets:"#aboutcontent",
        translateY:-50,
               opacity:[0,1],
               scale:[0.4,1],
        easing:'easeInOutSine'
    })
});
$("#aboutbuttonminimize").click(function (e) {
    $('#aboutmin').show();
    $('#mission').show();
    $('#vision').show();
    $('#aboutmax').hide();
    $('#aboutbuttonmore').show();
    $('#aboutbuttonminimize').hide();
    anime({
        targets:"#aboutcontent",
        translateY:0,
        scale:[0.4,1],
               opacity:[0,1],
        easing:'easeInOutSine'
    })
});
